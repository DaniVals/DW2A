<?php
	$a = 3;
	$b = "3";

	// El == compara si el contenido es el mismo (si son iguales)
	if ($a == $b){
		echo "Son iguales <br>";
	}else{
		echo "No son iguales <br>";
	}

	// El === compara si el contenido y el tipo son el mismo (si son idénticos)
	if ($a === $b){
		echo "Son idénticos <br>";
	}else{
		echo "No son idénticos <br>";
	}